export function getChefSprite(facing: Direction, walkFrame: number): number[][] {
  const frame = walkFrame % 2;
  switch (facing) {
    case "left":
      return frame === 0 ? CHEF_LEFT_1 : CHEF_LEFT_2;
    case "right":
      return frame === 0 ? CHEF_RIGHT_1 : CHEF_RIGHT_2;
    case "up":
    case "down":
      return frame === 0 ? CHEF_CLIMB_1 : CHEF_CLIMB_2;
    case "none":
    default:
      return CHEF_FRONT;
  }
}

export function getEnemySprite(type: EnemyType, walkFrame: number): number[][] {
  const frame = walkFrame % 2;
  switch (type) {
    case "hotdog":
      return frame === 0 ? HOTDOG_1 : HOTDOG_2;
    case "pickle":
      return frame === 0 ? PICKLE_1 : PICKLE_2;
    case "egg":
      return frame === 0 ? EGG_1 : EGG_2;
  }
}

export function getIngredientSprite(type: IngredientType): number[][] {
  switch (type) {
    case "bun-top":
      return BUN_TOP_SPRITE;
    case "bun-bottom":
      return BUN_BOTTOM_SPRITE;
    case "lettuce":
      return LETTUCE_SPRITE;
    case "meat":
      return MEAT_SPRITE;
    case "cheese":
      return CHEESE_SPRITE;
  }
}
